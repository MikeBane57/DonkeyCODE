#!/usr/bin/env node
/**
 * Writes DonkeyCode/baked-config.js from environment variables (not committed).
 * If no token is set, copies baked-config.template.js so the extension still loads.
 *
 * Env (optional):
 *   DONKEYCODE_GITHUB_TOKEN
 *   DONKEYCODE_GITHUB_OWNER
 *   DONKEYCODE_GITHUB_REPO
 *   DONKEYCODE_GITHUB_BRANCH   (default: main)
 *   DONKEYCODE_GITHUB_PATH     (default: sessions/donkeycode-sessions.json)
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const donkeyDir = path.join(root, "DonkeyCode");
const outFile = path.join(donkeyDir, "baked-config.js");
const templateFile = path.join(donkeyDir, "baked-config.template.js");

function escJsString(s) {
  return JSON.stringify(s == null ? "" : String(s));
}

const token = (process.env.DONKEYCODE_GITHUB_TOKEN || "").trim();
const owner = (process.env.DONKEYCODE_GITHUB_OWNER || "").trim();
const repo = (process.env.DONKEYCODE_GITHUB_REPO || "").trim();
const branch =
  (process.env.DONKEYCODE_GITHUB_BRANCH || "main").trim() || "main";
const filePath =
  (process.env.DONKEYCODE_GITHUB_PATH || "sessions/donkeycode-sessions.json")
    .trim()
    .replace(/^\/+/, "") || "sessions/donkeycode-sessions.json";

if (!fs.existsSync(templateFile)) {
  console.error("Missing:", templateFile);
  process.exit(1);
}

if (!token && !owner && !repo) {
  fs.copyFileSync(templateFile, outFile);
  console.log(
    "DonkeyCode: wrote",
    path.relative(root, outFile),
    "from template (set DONKEYCODE_GITHUB_* env vars to bake a team token)"
  );
  process.exit(0);
}

const body = `/**
 * AUTO-GENERATED by scripts/build-baked-config.mjs — do not commit (see .gitignore).
 * Re-run: npm run build
 */
self.BAKED_GITHUB_DEFAULTS = {
  token: ${escJsString(token)},
  owner: ${escJsString(owner)},
  repo: ${escJsString(repo)},
  branch: ${escJsString(branch)},
  path: ${escJsString(filePath)},
};
`;

fs.writeFileSync(outFile, body, "utf8");
console.log(
  "DonkeyCode: wrote",
  path.relative(root, outFile),
  "with baked GitHub defaults (token length:",
  token.length,
  ")"
);
